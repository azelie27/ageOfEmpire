class Human {

    int level;
    int age;

    public Human() {
    }

    public Human(int level, int age) {
        this.level = level;
        this.age = age;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    



   



}